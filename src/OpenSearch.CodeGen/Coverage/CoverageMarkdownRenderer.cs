using System.Text;

namespace OpenSearch.CodeGen.Coverage;

/// <summary>
/// Renders a <see cref="CoverageReport"/> to a deterministic Markdown document. The output is
/// committed as <c>API_COVERAGE.md</c> and byte-compared by a test, so formatting must be stable.
/// </summary>
public static class CoverageMarkdownRenderer
{
	public static string Render(CoverageReport report, string javaProvenance)
	{
		var sb = new StringBuilder();

		sb.AppendLine("# OpenSearch .NET — API coverage map");
		sb.AppendLine();
		sb.AppendLine("<!--");
		sb.AppendLine("  Generated by tests/OpenSearch.CodeGen.Tests/CoverageReportTests. Do not edit by hand.");
		sb.AppendLine("  Refresh after a spec or generator change:");
		sb.AppendLine("    UPDATE_COVERAGE=1 dotnet test tests/OpenSearch.CodeGen.Tests");
		sb.AppendLine($"  java cross-reference: {javaProvenance}");
		sb.AppendLine("-->");
		sb.AppendLine();
		sb.AppendLine("Coverage of the vendored OpenSearch API spec by this client, cross-referenced against the");
		sb.AppendLine("opensearch-java client. \"Covered\" means the generator emits a typed request/response, or a");
		sb.AppendLine("hand-written request type exists (NDJSON endpoints). A namespace is \"wired\" when it is in");
		sb.AppendLine("the generated set; every other spec namespace is a gap.");
		sb.AppendLine();

		// ---- Summary ---------------------------------------------------------
		var totalNs = report.Namespaces.Count;
		var generated = CountByStatus(report, CoverageStatus.Generated);
		var handWritten = CountByStatus(report, CoverageStatus.HandWritten);
		var ignorable = CountByStatus(report, CoverageStatus.Ignorable);
		var ndjsonMissing = CountByStatus(report, CoverageStatus.NdjsonMissing);
		var notWired = CountByStatus(report, CoverageStatus.NamespaceNotWired);

		sb.AppendLine("## Summary");
		sb.AppendLine();
		sb.AppendLine("| Metric | Count |");
		sb.AppendLine("| --- | ---: |");
		sb.AppendLine($"| Spec namespaces | {totalNs} |");
		sb.AppendLine($"| Wired (generated) | {report.WiredNamespaces} |");
		sb.AppendLine($"| Shipped by opensearch-java | {report.JavaNamespaces} |");
		sb.AppendLine($"| Operation groups (spec) | {report.TotalOperations} |");
		sb.AppendLine($"| Covered (generated + hand-written) | {report.CoveredOperations} |");
		sb.AppendLine($"| — generated | {generated} |");
		sb.AppendLine($"| — hand-written (NDJSON) | {handWritten} |");
		sb.AppendLine($"| Excluded (x-ignorable) | {ignorable} |");
		sb.AppendLine($"| Gap — NDJSON not hand-written | {ndjsonMissing} |");
		sb.AppendLine($"| Gap — namespace not wired | {notWired} |");
		sb.AppendLine();

		// ---- Namespace table -------------------------------------------------
		sb.AppendLine("## Namespaces");
		sb.AppendLine();
		sb.AppendLine("| Namespace | Status | Covered / Total | opensearch-java |");
		sb.AppendLine("| --- | --- | ---: | :---: |");
		foreach (var ns in report.Namespaces)
		{
			var status = !ns.Wired ? "not wired" : ns.Gaps > 0 ? "wired (gaps)" : "wired";
			var java = ns.InJavaClient ? "yes" : "—";
			sb.AppendLine($"| {ns.Namespace} | {status} | {ns.Covered} / {ns.Total} | {java} |");
		}
		sb.AppendLine();

		// ---- Gap inventory ---------------------------------------------------
		sb.AppendLine("## Gaps");
		sb.AppendLine();

		var notWiredNs = report.Namespaces.Where(n => !n.Wired).ToList();
		var javaHas = notWiredNs.Where(n => n.InJavaClient).ToList();
		var neither = notWiredNs.Where(n => !n.InJavaClient).ToList();

		sb.AppendLine("### Namespaces not wired — opensearch-java ships these (next priority)");
		sb.AppendLine();
		if (javaHas.Count == 0)
			sb.AppendLine("_None._");
		else
			foreach (var ns in javaHas)
				AppendNamespaceGapLine(sb, ns);
		sb.AppendLine();

		sb.AppendLine("### Namespaces not wired — neither client ships these");
		sb.AppendLine();
		if (neither.Count == 0)
			sb.AppendLine("_None._");
		else
			foreach (var ns in neither)
				AppendNamespaceGapLine(sb, ns);
		sb.AppendLine();

		sb.AppendLine("### NDJSON endpoints (excluded from codegen)");
		sb.AppendLine();
		var ndjson = report.Namespaces
			.SelectMany(n => n.Operations)
			.Where(o => o.Status is CoverageStatus.HandWritten or CoverageStatus.NdjsonMissing)
			.OrderBy(o => o.OperationGroup, StringComparer.Ordinal)
			.ToList();
		if (ndjson.Count == 0)
		{
			sb.AppendLine("_None._");
		}
		else
		{
			sb.AppendLine("| Operation group | Hand-written |");
			sb.AppendLine("| --- | :---: |");
			foreach (var op in ndjson)
				sb.AppendLine($"| {op.OperationGroup} | {(op.Status == CoverageStatus.HandWritten ? "yes" : "missing")} |");
		}
		sb.AppendLine();

		// Trailing newline only; trim any double blank at EOF for a stable byte tail.
		var text = sb.ToString().TrimEnd('\n', '\r');
		return text + "\n";
	}

	private static void AppendNamespaceGapLine(StringBuilder sb, NamespaceCoverage ns)
	{
		var groups = ns.Operations
			.OrderBy(o => o.OperationGroup, StringComparer.Ordinal)
			.Select(o => o.OperationGroup);
		sb.AppendLine($"- **{ns.Namespace}** ({ns.Total}): {string.Join(", ", groups)}");
	}

	private static int CountByStatus(CoverageReport report, CoverageStatus status) =>
		report.Namespaces.Sum(n => n.Operations.Count(o => o.Status == status));
}
